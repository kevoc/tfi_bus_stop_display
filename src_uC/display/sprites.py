
from framebuf import FrameBuffer, MONO_HLSB


# to make these sprites, draw or load them into:
#    https://lopaka.app/sandbox
# Library: Adafruit GFX Mono, Display: 128x64
#
# and then take the list of bytes from the generated c-code,
# wrap those bytes in a call to bytes([]), i.e.:
#               bytes([   0x80,0x0f,0x00,0x60.......   ])
# and execute that in a python terminal to turn it into a binary string.


def make_sprite(width: int, height: int, data: bytes):
    """Create a frame buffer with the sprite data in it."""

    buf = memoryview(bytearray(len(data)))
    buf[:] = data
    return FrameBuffer(buf, width, height, MONO_HLSB)


